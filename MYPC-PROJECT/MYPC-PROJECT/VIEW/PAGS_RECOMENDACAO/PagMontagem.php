        <section class="passos">
            <h2>Passos para Montar o Computador</h2>
            <ol>
                <li>
                    <h3>1. Reúna suas Ferramentas e Componentes</h3>
                    <p>Certifique-se de ter todos os componentes necessários: <strong>placa-mãe, processador, memória RAM, placa de vídeo, fonte de alimentação, armazenamento, gabinete e periféricos.</strong></p>
                </li>
                <li>
                    <h3>2. Prepare o Gabinete</h3>
                    <p>Instale as partes internas do gabinete, como suportes e fãs, se necessário.</p>
                </li>
                <li>
                    <h3>3. Instale o Processador na Placa-Mãe</h3>
                    <p>Coloque o processador no soquete da placa-mãe e fixe-o com o cooler apropriado.</p>
                </li>
                <li>
                    <h3>4. Instale a Memória RAM</h3>
                    <p>Insira os módulos de RAM nos slots da placa-mãe, certificando-se de que eles estejam firmemente encaixados.</p>
                </li>
                <li>
                    <h3>5. Instale a Placa de Vídeo</h3>
                    <p>Coloque a placa de vídeo no slot PCIe da placa-mãe e fixe-a com parafusos.</p>
                </li>
                <li>
                    <h3>6. Instale o Armazenamento</h3>
                    <p>Monte o SSD ou HD no compartimento apropriado do gabinete e conecte os cabos.</p>
                </li>
                <li>
                    <h3>7. Conecte os Cabos</h3>
                    <p>Conecte os cabos da fonte de alimentação, incluindo cabos de energia para a placa-mãe, processador e placa de vídeo.</p>
                </li>
                <li>
                    <h3>8. Feche o Gabinete e Teste</h3>
                    <p>Feche o gabinete e ligue o computador para verificar se tudo está funcionando corretamente.</p>
                </li>
            </ol>
        </section>

        <section class="tipos">
            <h2>Dicas Úteis</h2>
            <ul>
                <li>Verifique a compatibilidade dos componentes antes de comprar.</li>
                <li>Leia os manuais dos componentes para instruções específicas.</li>
                <li>Mantenha a área de trabalho limpa e organizada.</li>
                <li>Tenha uma boa ventilação durante a montagem para evitar superaquecimento.</li>
            </ul>
